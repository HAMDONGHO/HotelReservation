package instance;

public class Reserve {
	int id;
	String Hotel_name;
	int r_number;
	String check_in;
	String check_out;
	float total;
	
	public Reserve(int id , int r_id, String h_id, String check_in, String check_out)
	{
		this.id = id;
		this.r_number = r_id;
		this.Hotel_name = h_id; 
		this.check_in = check_in;
		this.check_out = check_out;
	}
	public Reserve()
	{}
	
	public int getid()
	{
		return id;
	}
	public void setid(int id)
	{
		this.id = id;
	}
	
	public String getHotel_name()
	{
		return Hotel_name;
	}
	public void setHotel_name(String Hotel_name)
	{
		this.Hotel_name = Hotel_name;
	}
	public int getroom_id()
	{
		return r_number;
	}
	public void setroom_id(int r_number)
	{
		this.r_number = r_number;
	}
	
	public String getcheck_in()
	{
		return check_in;
	}
	public void setcheck_in(String check_in)
	{
		this.check_in = check_in;
	}
	public String getcheck_out()
	{
		return check_out;
	}
	public void setcheck_out(String check_out)
	{
		this.check_out = check_out;
	}
	public float gettotal()
	{
		return total;
	}
	public void settotal(float total)
	{
		this.total = total;
	}
	
	
	public String toString()
	{
		return " *   id = " + id + ",    hotel_name = " + Hotel_name + ",    room_number = " + r_number + ",    check_in = " + check_in + ",    check_out = " + check_out + ",    total = "+ total + " *";
	}
}
