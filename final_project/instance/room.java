package instance;

public class room {
	int r_id;
	String grade;
	float ex_price;
	int hotel_id;
	
	public room(int r_id, String grade, float ex_price, int hotel_id)
	{

		this.r_id = r_id;
		this.grade = grade;
		this.ex_price = ex_price;
		this.hotel_id = hotel_id;

	}
	public room()
	{
	}
	
	public room(room room)
	{
		this.grade = room.grade;
		this.ex_price = room.ex_price;
	}
	
	public int getr_id()
	{
		return r_id;
	}
	public void setr_id(int r_id)
	{
		this.r_id = r_id;
	}
	
	public String getgrade()
	{
		return grade;
	}
	public void setgrade(String grade)
	{
		this.grade = grade;
	}
	public float getex_price()
	{
		return ex_price;
	}
	public void setex_price(float ex_price)
	{
		this.ex_price = ex_price;
	}

	public String toString()
	{
		return " *   r_id = " + r_id + ",    grade = " + grade + ",      ex_price = "+ ex_price + " *";
	}
}
